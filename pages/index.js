import Head from "next/head";
import { useEffect, useState } from "react";
import About from "../components/About/About";
import Apartments from "../components/Apartments/Apartments";
import Cart from "../components/Cart/Cart";
import MainHead from "../components/MainHead/MainHead";
import PriceRoom from "../components/PriceRoom/PriceRoom";
import Realters from "../components/Realters/Realters";
import Services from "../components/Services/Services";
import Upper from "../components/Upper/Upper";
import styles from "../styles/Home.module.sass";

export default function Home() {
  const [screenWidth, setScreenWidth] = useState(null);
  const handleWidth = () => {
    const screenWidth = window.screen.width;
    setScreenWidth(screenWidth);
  };
  useEffect(() => {
    handleWidth();
    window.addEventListener("resize", handleWidth);
    return () => window.removeEventListener("resize", handleWidth);
  }, []);
  return (
    <div className={styles.container}>
      <Head>
        <title>Главная</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <MainHead width={screenWidth} />
      <Cart width={screenWidth} />
      <About width={screenWidth} />
      <Services width={screenWidth} />
      <Apartments width={screenWidth} />
      <PriceRoom width={screenWidth} />
      <Realters width={screenWidth} />
      <Upper width={screenWidth} />
    </div>
  );
}
